; -----------------------------------------------------------------------------
section .text
; -------------------------------------

global start
extern kmain

start:
    mov     esp, stack.top
    call    kmain
.halt:
    hlt
    jmp     .halt
.end:
; -----------------------------------------------------------------------------



; -----------------------------------------------------------------------------
section .bss
; -------------------------------------
stack: align 16
.bottom:
    resb    0x1000
.top:
; -----------------------------------------------------------------------------
